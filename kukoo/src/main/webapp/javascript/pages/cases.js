function start_end_point(j, events) {
  if (j == 0 || (j - 1 > 0 && events[j - 1].isProvince == 1) && events[j].isUnion == 1) {
    events[j].svg = 1; //递交申请。联邦申请开始
  }
  if ((j + 1) < events.length && events[j + 1].isUnion == 1 && events[j].isProvince == 1) {
    events[j].svg = 2; //省提名结束
  }
  if (j == events.length - 1) {
    events[j].svg = 3; //联邦结束
  }
}


_app = new Vue({
  "el": '.main',
  "data": {
    "list": list,
    "country": country,
    "projectType": projectType,
    "project": project,
    "occup": occup,
    "ages": ages,
    "offset": 0,
    "selected": {
      "country": 0,
      "projectType": 0,
      "project": 0,
      "occup": 0,
      "ages": 0
    }
  },
  mounted: function() {},
  created: function() {
    for (var i = 0; i < this.list.length; i++) {
      var events = this.list[i].events;
      var first_p = -1;
      var first_u = -1;
      var len_p = 0;
      var len_u = 0;
      for (var j = 0; j < events.length; j++) {
        start_end_point(j, events);
        if (first_p == -1 && events[j].isProvince == 1) first_p = j;
        if (first_u == -1 && events[j].isUnion == 1) first_u = j;
        if (events[j].isProvince == 1) len_p++;
        if (events[j].isUnion == 1) len_u++;
      }
      this.list[i].strip = {
        "p": len_p > 0,
        "u": len_u > 0,
        "left_p": 0,
        "width_p": 86 / (events.length - 1) * (len_p - 1) + 6 + this.offset,
        "width_u": 86 / (events.length - 1) * (len_u - 1) + 6 + this.offset,
        "left_u": 86 / (events.length - 1) * len_p + 6 + this.offset,
      };
    }
  },
  methods: {
    search: function(e) {
      var t = $(e.target).parent().attr("t");
      var index = $(e.target).index();
      this.selected[t] = index;
      var _this = this;
      this.list = list.filter(function(cv, index, arr) {
        var mapping = {
          "country": "countryId",
          "projectType": "projectTypeId",
          "project": "projectId",
          "occup": "occupId",
          "ages": "age"
        }
        var rv = [];
        for (var item in _this.selected) {
          var selIndex = _this.selected[item];
          if (selIndex != 0) {
            rv.push(_this[item][selIndex].key == cv[mapping[item]]);
          }
        }
        return rv.every(function(cv) {
          return cv;
        });

      });
    },
    beforeEnter: function(el) {
      el.style.opacity = 0;
      el.style.marginLeft = "100%";
    },
    enter: function(el, done) {
      var delay = parseInt(el.dataset.index) * 500;
      setTimeout(function() {
        Velocity(
          el, {
            opacity: 1,
            marginLeft: 0
          }, {
            complete: done
          }
        )
      }, delay)
    },
    leave: function(el, done) {
      Velocity(
        el, {
          opacity: 0,
          marginLeft: "-100%"
        }, {
          complete: done
        }
      );
    }
  }
});

